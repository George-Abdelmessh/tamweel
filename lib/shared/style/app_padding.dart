import 'package:flutter/material.dart';

/// CONSTANTS APP PADDING
class AppPadding {
  /// 10 10 10 10
  static EdgeInsets padding10 = const EdgeInsets.all(10.0);

  /// 15 15 15 15
  static EdgeInsets padding15 = const EdgeInsets.all(15.0);

  /// 20 20 20 20
  static EdgeInsets padding20 = const EdgeInsets.all(20.0);

  /// 0 10 0 10
  static EdgeInsets paddingH10 = const EdgeInsets.symmetric(horizontal: 10.0);

  /// 0 15 0 15
  static EdgeInsets paddingH15 = const EdgeInsets.symmetric(horizontal: 15.0);

  /// 0 20 0 20
  static EdgeInsets paddingH20 = const EdgeInsets.symmetric(horizontal: 20.0);

  /// 10 0 10 0
  static EdgeInsets paddingV10 = const EdgeInsets.symmetric(vertical: 10.0);

  /// 20 0 20 0
  static EdgeInsets paddingV20 = const EdgeInsets.symmetric(vertical: 20.0);

  /// PADDING FOR TEXT FORM FIELD 20 5 20 5
  static EdgeInsets paddingTextFormField = const EdgeInsets.symmetric(
    vertical: 20.0,
    horizontal: 5.0,
  );
}
